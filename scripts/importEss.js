    console.log(`üìÇ –ß–∏—Ç–∞—é —Ñ–∞–π–ª: ${filePath}`);

    const fileBuffer = readFileSync(filePath);
    const workbook = XLSX.read(fileBuffer, { type: "buffer" });

    if (!workbook.SheetNames.includes("PRICE_ESS")) {
      throw new Error("–õ–∏—Å—Ç PRICE_ESS –Ω–µ –Ω–∞–π–¥–µ–Ω.");
    }

    const rows = XLSX.utils.sheet_to_json(workbook.Sheets["PRICE_ESS"]);
    console.log(`üìä –ù–∞–π–¥–µ–Ω–æ —Å—Ç—Ä–æ–∫: ${rows.length}`);

    const [category] = await db
      .select()
      .from(priceCategories)
      .where(eq(priceCategories.code, "ess"))
      .limit(1);

    if (!category) throw new Error("–ö–∞—Ç–µ–≥–æ—Ä–∏—è ess –Ω–µ –Ω–∞–π–¥–µ–Ω–∞!");

    console.log(`‚úÖ –ö–∞—Ç–µ–≥–æ—Ä–∏—è ess –Ω–∞–π–¥–µ–Ω–∞. ID = ${category.id}`);

